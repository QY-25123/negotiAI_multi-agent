def _build_styles() -> dict:
    base = getSampleStyleSheet()
    s = {}

    def ps(name, **kw) -> ParagraphStyle:
        return ParagraphStyle(name, parent=base["Normal"], **kw)

    s["logo"] = ps("logo", fontSize=18, fontName="Helvetica-Bold", textColor=WHITE, alignment=TA_LEFT)
    s["doc_type"] = ps("doc_type", fontSize=10, fontName="Helvetica-Bold", textColor=colors.HexColor("#c7d2fe"), alignment=TA_RIGHT)
    s["contract_title"] = ps("contract_title", fontSize=13, fontName="Helvetica-Bold", textColor=GREY_900, alignment=TA_LEFT)
    s["meta_right"] = ps("meta_right", fontSize=8, fontName="Helvetica", textColor=GREY_700, alignment=TA_RIGHT)
    s["section_title"] = ps("section_title", fontSize=8, fontName="Helvetica-Bold", textColor=BRAND_INDIGO, spaceBefore=0, spaceAfter=0, letterSpacing=1)
    s["party_header"] = ps("party_header", fontSize=7, fontName="Helvetica-Bold", textColor=BRAND_INDIGO)
    s["party_name"] = ps("party_name", fontSize=11, fontName="Helvetica-Bold", textColor=GREY_900)
    s["party_role"] = ps("party_role", fontSize=8, fontName="Helvetica", textColor=GREY_700)
    s["party_meta"] = ps("party_meta", fontSize=7, fontName="Helvetica-Oblique", textColor=GREY_500)
    s["clause"] = ps("clause", fontSize=8, fontName="Helvetica", textColor=GREY_700, leading=12)
    s["sig_label"] = ps("sig_label", fontSize=8, fontName="Helvetica-Bold", textColor=BRAND_INDIGO)
    s["sig_field_label"] = ps("sig_field_label", fontSize=7, fontName="Helvetica", textColor=GREY_500)
    s["sig_company"] = ps("sig_company", fontSize=8, fontName="Helvetica", textColor=GREY_700)
    s["footer"] = ps("footer", fontSize=7, fontName="Helvetica-Oblique", textColor=GREY_500, alignment=TA_CENTER)
    return s
